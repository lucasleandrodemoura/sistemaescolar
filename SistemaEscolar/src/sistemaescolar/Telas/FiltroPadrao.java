    public void alimentaTabela(String busca){
        
        for(int a = 0; a <100; a++){
            this.minhatabela.setValueAt("", a, 0);
            this.minhatabela.setValueAt("", a, 1);

        }
        
        
        String sql = "SELECT ID,"+this.campoLabel+" as descricao FROM  "+this.tabela
                + " WHERE "+this.campoLabel+" LIKE '%"+busca+"%'"
                
                + "ORDER BY ID DESC";
       
        DB db = new DB();
        try {
            ResultSet lerDados = db.lerDados(sql);
            int cont = 0;
            while(lerDados.next()){
                this.minhatabela.setValueAt(lerDados.getString("ID"), cont, 0);
                this.minhatabela.setValueAt(lerDados.getString("descricao"), cont, 1);

                cont++;
            }
            db.DesconectarBanco();
            
        } catch (SQLException ex) {
            Logger.getLogger(FiltroPadrao.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(FiltroPadrao.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

